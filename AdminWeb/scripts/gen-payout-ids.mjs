#!/usr/bin/env node
// gen-payout-ids.mjs — reads BlueprintData CSVs and emits src/generated/lookup-data.ts
// Run via: node scripts/gen-payout-ids.mjs
// Called automatically by: npm run build

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const projectRoot = resolve(__dirname, '..')
const blueprintRoot = resolve(__dirname, '../../../../Assets/Resources/BlueprintData')

function readCsv(filePath) {
  const text = readFileSync(filePath, 'utf8')
  const lines = text.split(/\r?\n/)
  if (lines.length < 2) return { headers: [], rows: [] }
  const headers = lines[0].split(',').map(h => h.trim())
  const rows = lines.slice(1)
    .filter(l => l.trim())
    .map(l => {
      const cols = l.split(',')
      const row = {}
      headers.forEach((h, i) => { row[h] = (cols[i] ?? '').trim() })
      return row
    })
  return { headers, rows }
}

// Resource.csv: all Id values → CURRENCY_IDS
const resourceCsv = readCsv(resolve(blueprintRoot, 'Resource.csv'))
const currencyIds = resourceCsv.rows.map(r => r['Id']).filter(Boolean)

// ItemDrop.csv: split by AssetType
const itemDropCsv = readCsv(resolve(blueprintRoot, 'ItemDrop.csv'))
const itemIds = itemDropCsv.rows
  .filter(r => r['AssetType'] === 'Item')
  .map(r => r['Id']).filter(Boolean)
const ticketIds = itemDropCsv.rows
  .filter(r => r['AssetType'] === 'Ticket')
  .map(r => r['Id']).filter(Boolean)

function toTsArray(ids) {
  return '[\n' + ids.map(id => `  '${id}',`).join('\n') + '\n]'
}

const output = `// AUTO-GENERATED by scripts/gen-payout-ids.mjs — do not edit manually.
// Source: Assets/Resources/BlueprintData/Resource.csv + ItemDrop.csv
// Commit this file; it is intentionally checked in so CI builds without running the script.

export const CURRENCY_IDS: string[] = ${toTsArray(currencyIds)}

export const ITEM_IDS: string[] = ${toTsArray(itemIds)}

export const TICKET_IDS: string[] = ${toTsArray(ticketIds)}
`

const outPath = resolve(projectRoot, 'src/generated/lookup-data.ts')
mkdirSync(dirname(outPath), { recursive: true })
writeFileSync(outPath, output, 'utf8')
console.log(`gen-payout-ids: wrote ${outPath}`)
console.log(`  CURRENCY_IDS: ${currencyIds.length}  ITEM_IDS: ${itemIds.length}  TICKET_IDS: ${ticketIds.length}`)
